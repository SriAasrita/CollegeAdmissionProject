package Admissions.Student;

public class Address {
	
	private String Address_Door_No;
	private String Address_Street;
	private String Address_City;
	private String Address_Pin_Code;
	
	public String getAddress_Door_No() {
		return Address_Door_No;
	}
	public void setAddress_Door_No(String address_Door_No) {
		Address_Door_No = address_Door_No;
	}
	public String getAddress_Street() {
		return Address_Street;
	}
	public void setAddress_Street(String address_Street) {
		Address_Street = address_Street;
	}
	public String getAddress_City() {
		return Address_City;
	}
	public void setAddress_City(String address_City) {
		Address_City = address_City;
	}
	public String getAddress_Pin_Code() {
		return Address_Pin_Code;
	}
	public void setAddress_Pin_Code(String address_Pin_Code) {
		Address_Pin_Code = address_Pin_Code;
	}
	
	
}
